//2B分包序号 2B总包序号 2B分包序号
//2B LEN
//4B ID 4B ID
//16B 0
//2B NUMBER
//3B 0
//4B 01
//5B UNKNOWN
//2B FLAG
int VoipTalkBox::IsVoipTalkBox(DATEHEADER& packh,const char *pPacket)		
{
	if( (packh.m_dport == 30000 || packh.m_sport == 30000) && (packh.m_type == TCPPacket) )			
	{
		if (packh.m_datalen == 128 /*&& (*(unsigned short*)(pPacket+ 6) == 0x2e || *(unsigned short*)(pPacket+ 6) == 0x2a)*/ ) 
		{		
			if (memcmp(pPacket + 16, "\x0\x0\x0\x0\x0\x0\x0\x0\x0\x0\x0\x0\x0\x0\x0\x0\x0", 16) == 0)
			{
				if ( (*(unsigned int*)(pPacket + 37) == 0x00000001) && (*(unsigned short*)(pPacket + 46) == 0x0001) )
				{
					if (memcmp(pPacket + 34,"\x00\x00\x00",3) == 0)
					{
						if (*(unsigned short*)(pPacket + 48) == 0x00001800 )	//发送信息前的提示包 *(unsigned short*)(pPacket + 48) == 0x1900
						{
							return TALKBOX_MESSAGE;
						}
						else if(*(unsigned int*)(pPacket + 48) == 0x04570000) 	//发送语音前的提示包	pPacket + 33包序号
						{
							return TALKBOX_AUDIO;
						}
						else if(*(unsigned short*)(pPacket + 48) == 0x0500 || *(unsigned short*)(pPacket + 48) == 0x0f00) 	//发送语音前的提示包	pPacket + 33包序号
						{
							return TALKBOX_COMPLETE;
						}
						return TALKBOX_OTHER;
					}
				}
			}
		}
		else if (*(unsigned short*)pPacket == packh.m_datalen - 2)
		{
			if (memcmp(pPacket + packh.m_datalen - strlen("zh_CN"), "zh_CN", strlen("zh_CN")) == 0)
			{
				return TALKBOX_LOGIN;
			}
		}
	}
	return FALSE;
}





int VoipTalkBox::ParseTalkBoxPacket( LPTALKBOXDATA pTalkBoxData )
{
	unsigned char * pTmpSrc = pTalkBoxData->pInputBuf;
	unsigned char * pTmpDst = pTalkBoxData->pOutputBuf;

	while (pTmpSrc < pTalkBoxData->pInputBuf + pTalkBoxData->uiInputLen)
	{
		unsigned short PackNum = *(unsigned short*)(pTmpSrc + 2);
		unsigned short SubPackCnt = *(unsigned short*)pTmpSrc;
		unsigned short SubPackNum = *(unsigned short*)(pTmpSrc + 4);

		if( (SubPackCnt == 0 ) && (*(unsigned short*)(pTmpSrc + 2) == *(unsigned short*)(pTmpSrc + 6)) && 
			(*(unsigned short*)(pTmpSrc + 8) == 0x78) )		//心跳随机插入包头或者包尾导致格式错乱
		{
			pTmpSrc += 2;
			PackNum = *(unsigned short*)(pTmpSrc + 2);
			SubPackCnt = *(unsigned short*)pTmpSrc;
			SubPackNum = *(unsigned short*)(pTmpSrc + 4);
		}

		if( (SubPackNum == SubPackCnt) && (SubPackNum == 1) )
		{
			pTalkBoxData->uiCallerID = *(unsigned int*)(pTmpSrc + 8);
			pTalkBoxData->uiCalleeID = *(unsigned int*)(pTmpSrc + 12);
			int MsgLen = *(unsigned short*)(pTmpSrc + 6) + 8 - 50;
			memmove(pTmpDst, pTmpSrc + 50, MsgLen);
			pTmpDst += MsgLen;
			pTmpSrc += 128;
			if (pTalkBoxData->uiFlag == TRUE)
			{
				break;
			}
		}
		else if( (SubPackNum == SubPackCnt) && (SubPackNum > 1) )
		{
			pTalkBoxData->uiCallerID = *(unsigned int*)(pTmpSrc + 8);
			pTalkBoxData->uiCalleeID = *(unsigned int*)(pTmpSrc + 12);
			int MsgLen = *(unsigned short*)(pTmpSrc + 6) + 8 - 50;
			memmove(pTmpDst, pTmpSrc + 50, MsgLen);
			pTmpDst += MsgLen;
			pTmpSrc += 128;
		}
		else if( (SubPackNum > 1) && (SubPackNum < SubPackCnt) )
		{
			int MsgLen = *(unsigned short*)(pTmpSrc + 6) + 8 - 8;
			memmove(pTmpDst, pTmpSrc + 8, MsgLen);
			pTmpDst += MsgLen;
			pTmpSrc += 128;
		}
		else if( (SubPackNum == 1) && (SubPackNum < SubPackCnt) )
		{
			int MsgLen = *(unsigned short*)(pTmpSrc + 6) + 8 - 8;
			memmove(pTmpDst, pTmpSrc + 8, MsgLen);
			pTmpDst += MsgLen;
			pTmpSrc += 128;
			if (pTalkBoxData->uiFlag == TRUE)
			{
				break;
			}
		}
		else
		{
			printf("Talkbox parse packet error!\n");
			return FALSE;
		}
	}
 	* pTmpDst = 0;
	pTalkBoxData->uiOutputLen = pTmpDst - pTalkBoxData->pOutputBuf;
	return TRUE;
}





int VoipTalkBox::DecodeTalkBoxMessage(unsigned char * DecodeBuf,const unsigned char * EncodeBuf, size_t EncodeSize)
{
	unsigned int Flag = FALSE;
	int x = 0, y = 0;
	unsigned char * DecodeBuffer = DecodeBuf;

	for (; EncodeSize ; EncodeSize --)
	{
		Flag = FALSE;
		for (y = 0; y < 256; y ++)
		{
			if (TalkboxTable[x][y] == * EncodeBuf)
			{
				* DecodeBuffer = y;
				Flag = TRUE;
				break;
			}
		}

		if (Flag == FALSE)	//找不到说明码表错误
		{
			* DecodeBuffer = TalkboxTable[x][0x20];
			printf("Talkbox table error!\n");
			//return FALSE;
		}
		
		DecodeBuffer ++;
		EncodeBuf ++;
		x ++;
		if (x == 7)
		{
			x = 0;
		}
	}
	return DecodeBuffer - DecodeBuf;
}




