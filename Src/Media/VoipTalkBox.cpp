//存在的问题要自己组包，是要双向处理，消息是单向的，可能有问题
int VoipTalkBox::InsertPacket(DATEHEADER & packh,const char * pPacket)
{
#ifdef _DEBUG
	if (packh.m_datalen == 2)
	{
		printf("Talkbox packet lenth 2!\n");
	}

	int Mod = packh.m_datalen % 128;
	if ( Mod == 2)
	{
		printf("Talkbox packet lenth mod 2!\n");	
	}
#endif

	//丢弃2字节数据包,可能是心跳包
	if( packh.m_dport != 30000 && packh.m_sport != 30000)
	{
		return FALSE;
	}

	if (packh.m_datalen <= 2 || packh.m_type != TCPPacket)
	{
		return FALSE;
	}


	//音频必须要过滤重包
	LPSENDERINFO InfoPtr = m_SenderInfoBuf; 
	for( int Count = TcpFltCnt; Count ; Count -- )
	{
		if( (InfoPtr->SenderAck == packh.m_ack) && (InfoPtr->SendSequence == packh.m_sequence) &&
			(InfoPtr->PackSize == packh.m_datalen) && 
			(InfoPtr->PackIndex == *(unsigned short*)(pPacket + 2))  )
		{
			return FALSE;
		}
		InfoPtr ++ ;
	}

	if( (unsigned int)m_SenderInfoPtr >= (unsigned int)m_SenderInfoBuf + TcpFltCnt * sizeof(LPSENDERINFO) )
	{
		m_SenderInfoPtr = m_SenderInfoBuf;
	}
	m_SenderInfoPtr->SenderAck		= packh.m_ack;
	m_SenderInfoPtr->SendSequence	= packh.m_sequence;
	m_SenderInfoPtr->PackSize		= packh.m_datalen;
	m_SenderInfoPtr->PackIndex		= *(unsigned short*)(pPacket + 2);
	m_SenderInfoPtr ++;


	//区分信息和语音
	int Result = IsVoipTalkBox(packh,pPacket);
	if (Result == TALKBOX_LOGIN)
	{
		ProcLoginPack(packh, pPacket);
		m_Opcode = 0;
		return TRUE;
	}
	else if (Result == TALKBOX_MESSAGE || Result == TALKBOX_AUDIO)
	{
		m_from.Format("%d",*(unsigned int*)(pPacket + 8) );
		m_to.Format("%d",*(unsigned int*)(pPacket + 12) );
		m_Opcode = Result;
		ListToFile();
		return TRUE;
	}
	else if (Result == TALKBOX_COMPLETE /*|| Result == TALKBOX_OTHER*/)
	{
		m_from.Format("%d",*(unsigned int*)(pPacket + 8) );
		m_to.Format("%d",*(unsigned int*)(pPacket + 12) );
		m_Opcode = 0;
		ListToFile();
		return TRUE;
	}

	//解析消息数据包
	if( (m_Opcode == TALKBOX_MESSAGE)  || (*(unsigned short*)(pPacket + 48) == 0x1900) )	
	{
 		unsigned short PackNum = *(unsigned short*)(pPacket + 2);
 		unsigned short SubPackCnt = *(unsigned short*)pPacket;
 		unsigned short SubPackNum = *(unsigned short*)(pPacket + 4);
 		unsigned int Flag = FALSE;
 
		if( (*(unsigned short*)pPacket == 0) && (*(unsigned short*)(pPacket + 2) == *(unsigned short*)(pPacket + 6)) &&	//前面有2个0
			(*(unsigned short*)(pPacket + 8) == 0x78) )		
		{
			pPacket += 2;
			PackNum = *(unsigned short*)(pPacket + 2);
			SubPackCnt = *(unsigned short*)pPacket;
			SubPackNum = *(unsigned short*)(pPacket + 4);
		}

 		if( (SubPackNum == SubPackCnt) && (SubPackNum == 1) )		//单包数据
 		{
 			int MsgLen = *(unsigned short*)(pPacket + 6) + 8 - 50;
 			memmove(m_MsgBufPtr, pPacket + 50, MsgLen);
 			m_MsgBufPtr += MsgLen;
 			Flag = TRUE;
 		}
 		else if( (SubPackNum == SubPackCnt) && (SubPackNum > 1) )	//数据被分包的第一个包 只要被分包 第一个只有一个段
 		{
 			int MsgLen = *(unsigned short*)(pPacket + 6) + 8 - 50;
 			memmove(m_MsgBufPtr, pPacket + 50, MsgLen);
 			m_MsgBufPtr += MsgLen;
 		}
 		else 
 		{
 			const char * pTmp = pPacket;
 			for (int Cnt = SubPackNum; Cnt; Cnt --)
 			{
 				SubPackCnt = *(unsigned short*)pTmp;
 				SubPackNum = *(unsigned short*)(pTmp + 4);				
 				if( (SubPackNum > 1) && (SubPackNum < SubPackCnt) )
 				{
 					int MsgLen = *(unsigned short*)(pTmp + 6) + 8 - 8;
 					memmove(m_MsgBufPtr, pTmp + 8, MsgLen);
 					m_MsgBufPtr += MsgLen;
 					pTmp += 128;
 				}
 				else if( (SubPackNum == 1) && (SubPackNum < SubPackCnt) )
 				{
 					int MsgLen = *(unsigned short*)(pTmp + 6) + 8 - 8;
 					memmove(m_MsgBufPtr, pTmp + 8, MsgLen);
 					m_MsgBufPtr += MsgLen;
 					pTmp += 128;
 					Flag = TRUE;
 				}
				else
				{
					printf("parse talkbox packet error!\n");
					return FALSE;
				}
 			}
 		}


 		if( (m_MsgBufPtr - m_MsgBuf > 0) && (Flag == TRUE) )
 		{
 			*(unsigned int*)m_MsgBufPtr = 0;
 			int len = DecodeTalkBoxMessage(m_MsgBuf,m_MsgBuf,m_MsgBufPtr - m_MsgBuf);
 			Utf8ToGB2312((char*)m_MsgBuf);
 			m_indexfname = CreateMediaFilename("media_voip_talkbox.indexfile.txt",m_sip, m_dip);	
 			CString txt;
 			txt.Format("from=%s\r\nto=%s\r\nstate=message\r\nmsg=%s\r\n",m_from,m_to,m_MsgBuf);
 			WriteIndexFile("media_voip_talkbox", "media_voip_talkbox", txt, g_MediaMissObj);
 			m_Opcode = 0;
 			m_MsgBufPtr = m_MsgBuf;
			Flag = FALSE;
 			return TRUE;
 		}
	}
	else if (m_Opcode == TALKBOX_AUDIO)					//音频数据必须为单向
	{
		if( (*(unsigned int*)packh.m_sip != m_CallerIP) || (*(unsigned int*)packh.m_dip != m_CalleeIP) ||
			(packh.m_sport != m_CallerPORT) || (packh.m_dport != m_CalleePORT) )
		{
			if (m_SenderAudioBufPtr + packh.m_datalen >= m_SenderAudioBuf + AUDIOBUF_SIZE)
			{
				m_SenderAudioBufPtr = m_SenderAudioBuf;
			}
			memmove(m_SenderAudioBufPtr, pPacket, packh.m_datalen);
			m_SenderAudioBufPtr += packh.m_datalen;
		}
		else if( (*(unsigned int*)packh.m_dip != m_CallerIP) || (*(unsigned int*)packh.m_sip != m_CalleeIP) ||
			(packh.m_dport != m_CallerPORT) || (packh.m_sport != m_CalleePORT) )
		{
			if (m_RecverAudioBufPtr + packh.m_datalen >= m_RecverAudioBuf + AUDIOBUF_SIZE)
			{
				m_RecverAudioBufPtr = m_RecverAudioBuf;
			}
			memmove(m_RecverAudioBufPtr, pPacket, packh.m_datalen);
			m_RecverAudioBufPtr += packh.m_datalen;
		}
	}

	m_lastpacktm = packh.m_tm;
//	m_closetm = packh.m_tm + 60;
// 	m_endfiletm = packh.m_tm + 60;
	return TRUE;
}



